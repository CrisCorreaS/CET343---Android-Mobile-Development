package com.cristina.correa.exercisesweek1;

public class BasicIntegerCalculations1 implements Maths {
    public int integerAddition(int number1, int number2) {
        int result = number1 + number2;
        return result;
    }

    public int integerSubtraction(int number1, int number2) {
        int result = number1 - number2;
        return result;
    }

    public int integerMultiplication(int number1, int number2) {
        int result = number1 * number2;
        return result;
    }

    public float integerDivision(int number1, int number2) {
        float result = (float) number1 / number2;
        return result;
    }

}
